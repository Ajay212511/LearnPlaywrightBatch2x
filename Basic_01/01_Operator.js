let x=10; //here x is a variable and 10 is a value assigned to the variable x
//Assignment operator (=) is used to assign a value to a variable
console.log("value of x :"+x); // console.log() is used to print the value of x in the console

//ARITHMETIC OPERATORS
let a=5;
let b=20;
let c=a+b;  // here + is an operator which is used to perform addition operation on a and b and assign the result to c
console.log("value of c :"+c);
let d=a-b;                  // here - is an operator which is used to perform subtraction operation on a and b and assign the result to d
console.log("value of d :"+d);
let e=a*b;                  // here * is an operator which is used to perform multiplication operation on a and b and assign the result to e
console.log("value of e :"+e);
let f=a/b;                // here / is an operator which is used to perform division operation on a and b and assign the result to f        
console.log("value of f :"+f);

//MODULUS AND EXPONENTIATION OPERATORS

let g=a%b;
console.log("value of g :"+g); // here % is an operator which is used to perform modulus operation on a and b 
// and assign the result to g
let h=a**b;
console.log("value of h :"+h); // here ** is an operator which is used to perform exponentiation 
// operation on a and b and assign the result to h        
